// 命名空间：FC26.Settings
// 作用：注释型脚本（不参与运行时逻辑），说明 FC 26 工程的完整运行步骤。
//       包含 Unity 版本、URP 配置、Layer/Tag、场景加载、键位说明等。
// 备注：本脚本不包含任何可执行逻辑，仅作为运行手册。
//       首次运行前请先阅读本文件，并配合 ProjectSetupNote.cs 完成工程配置。
//=============================================================================
#if UNITY_EDITOR
using UnityEngine;

namespace FC26.Settings
{
    /// <summary>
    /// 工程运行说明（注释型脚本）。
    /// 首次运行请按本类内注释逐项操作。
    /// </summary>
    public class RunInstructions : MonoBehaviour
    {
        //================================================================================
        // 一、环境准备
        //================================================================================
        // 1. 安装 Unity 2022 LTS（推荐 2022.3.x 最新补丁版本）
        //    下载地址：https://unity.com/releases/editor/whats-new/2022.3.0
        // 2. 安装时勾选以下模块：
        //    - Build Support: Windows / macOS (根据本机系统)
        //    - Documentation
        // 3. 首次打开工程会自动编译所有脚本，等待编译完成（无报错即可）。
        //================================================================================


        //================================================================================
        // 二、URP 配置（首次打开工程后）
        //================================================================================
        // 1. 打开 Edit > Project Settings > Graphics
        // 2. 将 Assets/_FC26/Settings/FC26_URPAsset 拖入 Scriptable Render Pipeline Settings
        //    （若该资产不存在，按 ProjectSetupNote.cs 第二节创建）
        // 3. 打开 Edit > Project Settings > Quality，确认默认等级为 High
        // 4. 打开 Edit > Project Settings > Player > Color Space = Linear
        //================================================================================


        //================================================================================
        // 三、Layer / Tag 配置（首次打开工程后）
        //================================================================================
        // 按 ProjectSetupNote.cs 第四、五节配置 Layer（Field/Player/Ball/Goal/UI 等）
        // 与 Tag（GameManager/Bootstrapper/MainCamera 等）。
        // 若未配置，工程仍可运行但物理碰撞与摄像机 Culling Mask 可能不正确。
        //================================================================================


        //================================================================================
        // 四、生成工程目录结构（可选，推荐首次执行）
        //================================================================================
        // 1. 在 Unity 顶部菜单栏点击 "FC26 > 生成工程目录结构"
        // 2. 弹窗确认后，Assets/_FC26 下会自动创建 Scripts、Prefabs、Materials、
        //    Scenes、ScriptableObjects、Settings 等目录及子模块目录。
        // 3. 该操作幂等，重复执行不会删除已有文件。
        //================================================================================


        //================================================================================
        // 五、场景加载与运行
        //================================================================================
        // 1. 打开 Assets/_FC26/Scenes/Match.unity（双击场景文件）
        //    若该场景不存在，请按 ProjectSetupNote.cs 第八节创建。
        // 2. 确认场景中包含以下 GameObject：
        //    - GameManager（挂载 GameManager 脚本）
        //    - SceneBootstrapper（挂载 SceneBootstrapper 脚本）
        //    - EventSystem（UI 交互必需，若无则右键 UI > Event System 创建）
        // 3. 点击 Unity 顶部 ▶ Play 按钮进入运行模式
        // 4. 运行后预期表现：
        //    - SceneBootstrapper 在 Awake 阶段按顺序构建球场、灯光、摄像机、球员、
        //      足球、裁判、观众、UI（缺失模块会以占位对象替代并告警）
        //    - GameManager 进入初始状态（默认 MainMenu）
        //    - 控制台输出 "[GameManager] 初始化完成" 与各构建步骤日志
        // 5. 再次点击 ▶ 停止运行。
        //================================================================================


        //================================================================================
        // 六、键位说明（仅键鼠，不支持手柄）
        //================================================================================
        // 主键位（Windows 与 macOS 一致）：
        //
        //   [跑动]
        //     W / A / S / D   : 控制球员跑动（前/左/后/右，相对摄像机方向）
        //                       注意：WASD 仅用于跑动，不与任何动作键复用，避免键位冲突
        //
        //   [传球与直塞]
        //     鼠标左键        : 传球（按住时长决定力度，可长传）
        //     Space           : 直塞（球速更快、角度更前）
        //
        //   [射门]
        //     鼠标右键        : 射门（按住时长决定力度，力度条可视化）
        //
        //   [防守动作]
        //     Left Shift      : 铲断
        //     Left Ctrl       : 拼抢
        //     备注：macOS 上 Left Ctrl 可能与系统快捷键冲突，可在 KeyBindings 中
        //           重绑定为 Left Command 或其他键。
        //
        //   [切换球员]
        //     Q               : 切换到上一名队友（按距离或方向）
        //     E               : 切换到下一名队友
        //
        //   [镜头]
        //     鼠标滚轮        : 缩放摄像机距离与俯角（平滑过渡）
        //
        //   [暂停]
        //     Esc             : 暂停/恢复比赛（呼出暂停菜单，UI 仍响应）
        //
        // 跨平台说明：
        //   - 运行时通过 Application.platform 检测平台
        //   - macOS 上易冲突的组合键提供替代键
        //   - 所有键位可在 Input 模块的 KeyBindings 中重绑定
        //================================================================================


        //================================================================================
        // 七、常见问题排查
        //================================================================================
        // Q1: 运行后场景全黑或粉色（材质丢失）？
        // A1: 检查 URP Asset 是否已配置到 Graphics Settings；检查材质是否使用 URP/Lit Shader。
        //
        // Q2: 控制台报 "StadiumBuilder 未找到" 等告警？
        // A2: 正常现象。SceneBootstrapper 通过反射查找各模块 Builder，缺失时使用占位对象。
        //     实现对应模块后告警消失。
        //
        // Q3: 球员/UI 不显示？
        // A3: 检查 Player 模块与 UI 模块是否已实现并注册到 GameManager.StateMachine。
        //     Core 模块仅提供框架，具体功能由各模块实现。
        //
        // Q4: 物理碰撞异常？
        // A4: 检查 Physics Layer Collision Matrix 是否按 ProjectSetupNote.cs 第六节配置。
        //
        // Q5: 帧率低于 60？
        // A5: 检查 Quality Settings、Shadow Distance、是否有未优化的 Update 逻辑。
        //     可临时关闭后处理与阴影验证。
        //================================================================================


        //================================================================================
        // 八、开发流程建议
        //================================================================================
        // 1. 首次打开工程：执行菜单 "FC26 > 生成工程目录结构"
        // 2. 按 ProjectSetupNote.cs 配置 URP / Layer / Tag
        // 3. 实现 Core 模块（本模块已提供）
        // 4. 按 Data -> Input -> Stadium -> Player -> Ball -> AI -> Referee -> UI -> Camera
        //    顺序实现各功能模块
        // 5. 每实现一个模块，将其 Builder 注册到 SceneBootstrapper（挂载到场景或通过反射）
        // 6. 在 GameManager.Awake 中调用 StateMachine.RegisterState 注册各状态实现
        //================================================================================
    }
}
#endif
